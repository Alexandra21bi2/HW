#include "Header.h"

void swap(int *a1, int *b1) //функция для изменения значений переменных по адресам переменных
{
	int c = *a1; //обмен значений переменных по адресам
	*a1 = *b1;
	*b1 = c;

}
int main()
{
	int a = 8;
	int b = 7;
	std::cout << "a = " << a << ", b = " << b << std::endl;
	swap(&a, &b); // Вызов функции с использованием адресов фактических переменных
	std::cout << "a = " << a << ", b = " << b << std::endl;
}